num1=int(input("please enter number1: "))
num2=int(input("please enter number2: "))
num3=int(input("please enter number3: "))



operation = input("Please enter (+, -, * or /): ")

if operation == "+":
    print(num1 + num2)
elif operation == "-":
    print(num1 - num3)
elif operation == "*":
    print(num1 * num2)
elif operation == "/":
    print(num1 / num3)







